 public void recargar(ArrayList<AlmacenBE> almacenes){
            DefaultTableModel modelo=(DefaultTableModel) dgvAlmacen.getModel();
            for(int i=modelo.getRowCount()-1; i>=0; i--){
                modelo.removeRow(i);
            }

            dgvAlmacen.clearSelection();

            for(int i=0;i<almacenes.size();i++){
                 modelo.addRow(new Object[4]);
                dgvAlmacen.setValueAt(almacenes.get(i).getIdAlmacen(),i,0 );
                dgvAlmacen.setValueAt(almacenes.get(i).getIdentificador(),i,1 );
                dgvAlmacen.setValueAt(almacenes.get(i).getNombre(),i,2 );
            }
    }


